    <td><a class="btn text-primary btn-sm" onclick="editSubcategory('${i-1}')" data-bs-toggle="tooltip" data-bs-original-title="Edit"><span class="fe fe-edit fs-14"></span></a>
    </td>
    </tr>
    `;
    i++;
});
$("#bodycat").html(tabledata);
